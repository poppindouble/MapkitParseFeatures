    override func tableView(tableView: UITableView, cellForRowAtIndexPath indexPath: NSIndexPath) -> UITableViewCell {
        let cell = tableView.dequeueReusableCellWithIdentifier("myCell", forIndexPath: indexPath) as! TableViewCell
        cell.weBeamUser = weBeamUsers[indexPath.section][indexPath.row]
        return cell
    }
    
    override func tableView(tableView: UITableView, didSelectRowAtIndexPath indexPath: NSIndexPath) {
        performSegueWithIdentifier("TableCellSegue", sender: indexPath)
    }
    
    
    override func prepareForSegue(segue: UIStoryboardSegue, sender: AnyObject?) {
        let indexPath = sender as! NSIndexPath
        if segue.identifier == "TableCellSegue" {
            let detaillViewController = segue.destinationViewController as! DetailViewController
            detaillViewController.weBeamUser = weBeamUsers[indexPath.section][indexPath.row]
        }
    }
    
    
    

